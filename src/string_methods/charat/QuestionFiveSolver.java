package string_methods.charat;
/*
Question 5: Accessing a Character in a Specific Position

Task:
Given a string "Developers", use the charAt() method to print the character at the fifth position (index 4).

Expected Output:

l
 */

public class QuestionFiveSolver {
    public static void main(String[] args) {
        String title = "Developers";
        System.out.println(title.charAt(4));
    }
}
