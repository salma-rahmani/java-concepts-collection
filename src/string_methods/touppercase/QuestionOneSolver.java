package string_methods.touppercase;

/*
Question 1: Basic Conversion

Task:
Given a string "hello world", use the toUpperCase() method to convert the entire string to uppercase.
 Print the result.

Expected Output:

HELLO WORLD
 */
public class QuestionOneSolver {
    public static void main(String[] args) {
        String message = "hello world";
        System.out.println(message.toUpperCase());
    }
}
