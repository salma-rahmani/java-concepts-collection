package string_methods.equals;
/*
Question 3: Comparing with an Empty String

Task:
Given a string " " (a single space) and an empty string "", use the equals() method to check if they are equal. Print the result.

Expected Output:

false
 */
public class QuestionThreeSolver {
    public static void main(String[] args) {
        String text1 = " ";
        String text2 = "";
        System.out.println(text1.equals(text2));
    }
}
