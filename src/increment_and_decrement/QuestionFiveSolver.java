package increment_and_decrement;
/*
Question 5: Pre-Increment

Task:
Given an int variable c = 3, use pre-increment (++c) and then print c. Explain how pre-increment works compared to post-increment.

Expected Output:

4
 */
public class QuestionFiveSolver {
    public static void main(String[] args) {
        int c = 3;
        System.out.println(++c); // pre-incremented so it is 4

    }
}
