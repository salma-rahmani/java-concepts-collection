package increment_and_decrement;
/*
Question 1: Basic Increment

Task:
Given an int variable x = 5, increment x by 1 using the ++ operator. Print the result.

Expected Output:

6
 */

public class QuestionOneSolver {
    public static void main(String[] args) {
        int x = 5;
        x++; // The value of x is incremented by 1, making it 6.
        System.out.println(x);
    }
}
