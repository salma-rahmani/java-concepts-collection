package increment_and_decrement;
/*
Question 2: Basic Decrement

Task:
Given an int variable y = 10, decrement y by 1 using the -- operator. Print the result.

Expected Output:

9
 */

public class QuestionTwoSolver {
    public static void main(String[] args) {
        int y = 10;
        y--;
        System.out.println(y);
    }
}
